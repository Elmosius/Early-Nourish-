export const getWHOStandard = (ageInMonths, gender, type) => {
  const genderKey = gender === "l" ? "male" : "female";
  const data = WHO_DATA[type][genderKey];

  const closest =
    data.find((item) => item.month === Math.floor(ageInMonths)) ||
    data[data.length - 1];

  return closest;
};

export const calculateZScore = (actualValue, ageInMonths, gender, type) => {
  const standard = getWHOStandard(ageInMonths, gender, type);
  if (!standard) return null;

  const median = standard.M || standard.SD0;
  const sd =
    type === "weight"
      ? standard.SD1 - standard.SD0
      : standard.SD1 - standard.SD0;

  return ((actualValue - median) / sd).toFixed(2);
};

export const getNutritionalStatus = (
  actualValue,
  ageInMonths,
  gender,
  type,
) => {
  const standard = getWHOStandard(ageInMonths, gender, type);
  if (!standard) return "normal";

  if (actualValue < standard.SD2neg) return "severely-underweight";
  if (actualValue < standard.SD1neg) return "underweight";
  if (actualValue > standard.SD2) return "overweight";
  if (actualValue > standard.SD3) return "obese";

  return "normal";
};

